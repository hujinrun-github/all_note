import fs from 'node:fs'
import os from 'node:os'
import path from 'node:path'

const apiBase = (process.env.FLOWSPACE_API_URL ?? 'http://127.0.0.1:8080/api').replace(/\/$/, '')
const vault = fs.mkdtempSync(path.join(os.tmpdir(), 'flowspace-vault-'))

async function api(pathname, options = {}) {
  const response = await fetch(`${apiBase}${pathname}`, {
    method: options.method ?? 'GET',
    headers: options.body ? { 'Content-Type': 'application/json' } : undefined,
    body: options.body ? JSON.stringify(options.body) : undefined,
  })
  const payload = await response.json().catch(() => ({}))
  if (!response.ok) {
    const message = payload?.error?.message ?? response.statusText
    throw new Error(`${options.method ?? 'GET'} ${pathname} failed: ${message}`)
  }
  return payload.data
}

const noteData = await api('/notes', {
  method: 'POST',
  body: {
    title: 'Obsidian Sync Smoke',
    body: 'This note was generated by the FlowSpace Obsidian sync smoke test.',
    folder_id: '__uncategorized',
    tags: '["smoke","obsidian"]',
  },
})
const note = noteData.note

const targetInput = {
  name: 'Smoke Vault',
  vault_path: vault,
  base_folder: 'FlowSpace Notes',
  enabled: true,
  auto_sync: false,
}

await api('/sync/targets', { method: 'POST', body: targetInput })
await api('/sync/obsidian/test', { method: 'POST', body: targetInput })
const syncData = await api(`/sync/obsidian/notes/${note.id}`, { method: 'POST' })

const syncDir = path.join(vault, 'FlowSpace Notes')
const files = fs.existsSync(syncDir) ? fs.readdirSync(syncDir).filter((name) => name.endsWith('.md')) : []
if (files.length === 0) {
  throw new Error(`Expected a synced Markdown file in ${syncDir}`)
}

const syncedPath = syncData.item?.external_path ?? path.join(syncDir, files[0])
const markdown = fs.readFileSync(syncedPath, 'utf8')
for (const expected of [`id: ${note.id}`, 'source: flowspace', '# Obsidian Sync Smoke']) {
  if (!markdown.includes(expected)) {
    throw new Error(`Synced Markdown is missing ${JSON.stringify(expected)}`)
  }
}

console.log(JSON.stringify({
  apiBase,
  vault,
  note_id: note.id,
  external_path: syncedPath,
  files: files.length,
}, null, 2))
